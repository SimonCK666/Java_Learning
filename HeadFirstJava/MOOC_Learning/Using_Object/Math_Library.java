public class Math_Library {
    public static void main(String[] args) {
        System.out.println(Math.abs(-12));
        // 0-1之间的随机数
        System.out.println(Math.random());
        // 把小数部分四舍五入
        System.out.println(Math.round(10.234));
        // 2的3次方
        System.out.println(Math.pow(2, 3));
    }
}